///////////////////////////////////////////////////////////////////////////////////////

struct UAREA
{
    char ProcessName[20];                   // .exe name as it becomes process(dummy)
    PFILETABLE UFDT[MAXOPENFILES];          // user file descriptor table and it is an pointer array
};

